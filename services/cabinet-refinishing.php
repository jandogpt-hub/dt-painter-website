<script>
/**
 * Initializes all before/after comparison sliders on the page.
 * Supports both mouse and touch events.
 */
document.addEventListener('DOMContentLoaded', function () {
    const sliders = document.querySelectorAll('.comparison-slider');

    sliders.forEach(function (slider) {
        const afterImage = slider.querySelector('.comparison-after');
        const handle = slider.querySelector('.comparison-handle');
        let isDragging = false;

        /**
         * Gets the horizontal position of a mouse or touch event
         * relative to the slider's left edge, clamped to slider width.
         * @param {MouseEvent|TouchEvent} e
         * @returns {number}
         */
        function getPosition(e) {
            const rect = slider.getBoundingClientRect();
            const clientX = e.touches ? e.touches[0].clientX : e.clientX;
            return Math.max(0, Math.min(clientX - rect.left, rect.width));
        }

        /**
         * Updates the slider position based on pixel offset from left.
         * @param {number} x - Pixel offset from slider left edge.
         */
        function updateSlider(x) {
            const pct = (x / slider.offsetWidth) * 100;
            afterImage.style.width = pct + '%';
            handle.style.left = pct + '%';
        }

        /** Starts drag interaction. */
        function startDrag() { isDragging = true; }

        /** Stops drag interaction. */
        function stopDrag() { isDragging = false; }

        /**
         * Handles drag movement for both mouse and touch.
         * @param {MouseEvent|TouchEvent} e
         */
        function moveDrag(e) {
            if (!isDragging) return;
            if (e.cancelable) e.preventDefault();
            updateSlider(getPosition(e));
        }

        handle.addEventListener('touchstart', startDrag, { passive: false });
        slider.addEventListener('touchmove', moveDrag, { passive: false });
        window.addEventListener('touchend', stopDrag);

        handle.addEventListener('mousedown', startDrag);
        window.addEventListener('mousemove', moveDrag);
        window.addEventListener('mouseup', stopDrag);

        slider.addEventListener('click', function (e) {
            updateSlider(getPosition(e));
        });
    });
});
</script>
